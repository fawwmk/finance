#!/bin/bash
#
# Ежемесячная сверка налоговых ставок с открытыми источниками.
#
# Запускается сам, по расписанию (launchd), без участия человека.
# Что делает:
#   1. Читает текущий tax-config.json
#   2. Идёт в интернет и сверяет каждую цифру с nalog.gov.ru и обзорами
#      бухгалтерских сервисов
#   3. Если что-то разошлось — правит tax-config.json и пишет отчёт
#   4. НЕ пушит в GitHub. Изменения остаются локально, чтобы ты сначала
#      увидел их глазами в VS Code (вкладка Source Control) и только потом
#      нажал Sync. Правка налоговых цифр без просмотра — плохая идея.
#
# Отчёт: tax-rates-report.md в корне проекта.

set -uo pipefail

PROJECT="/Users/matveybolshakov/Desktop/finance"
CLAUDE="/Users/matveybolshakov/.local/bin/claude"
LOG="$PROJECT/scripts/tax-check.log"

cd "$PROJECT" || exit 1

# Задание собираем в переменную заранее — так heredoc не конфликтует
# с переносами строк в самой команде запуска.
read -r -d '' TASK <<'PROMPT'
Ты — автоматическая ежемесячная проверка налоговых ставок для приложения учёта финансов.

ЗАДАЧА
1. Прочитай файл tax-config.json в корне проекта. В нём текущие налоговые ставки РФ, которые использует приложение.

2. Проверь КАЖДУЮ цифру через веб-поиск по открытым источникам. Проверяй именно на текущий год.
   Что проверять:
     - фиксированные страховые взносы ИП «за себя» и потолок дополнительного 1%
     - порог дохода для НДС на УСН и ставки НДС
     - лимит дохода для патента (ПСН)
     - лимит дохода для НПД (самозанятые) и ставки 4%/6%
     - МРОТ и единая предельная база по взносам за работников
     - тарифы взносов за работников (общий и льготный МСП)
     - ступени прогрессивной шкалы НДФЛ и ставка для нерезидентов
   Приоритет источников: nalog.gov.ru → тексты законов → обзоры Контур, БУХ.1С, Бухонлайн, Главбух.
   Одной статьи мало: подтверждай цифру минимум двумя независимыми источниками.

3. Если ВСЁ совпадает — не меняй tax-config.json. Запиши в tax-rates-report.md короткий отчёт «всё актуально» с датой проверки и списком того, что сверил.

4. Если что-то РАЗОШЛОСЬ:
   - Обнови соответствующие значения в tax-config.json
   - Обнови в нём поля updatedAt (сегодняшняя дата) и source (на что опирался)
   - Запиши в tax-rates-report.md отчёт на русском языке в таком виде:
       # Изменения в налоговых ставках
       Дата проверки: YYYY-MM-DD
       ## Что изменилось
       - <Название>: было X → стало Y. Причина/закон. Ссылка на источник.
       ## Что проверено и не изменилось
       - краткий список
       ## Что делать
       Открой VS Code → Source Control → посмотри изменения в tax-config.json → нажми Sync.
       Приложение подтянет новые ставки в течение суток.

ВАЖНО
- Если цифру не удалось надёжно подтвердить двумя источниками — НЕ меняй её. Вместо этого напиши в отчёт «требует ручной проверки» с объяснением. Ошибиться в налоговой цифре хуже, чем оставить старую.
- Не трогай никакие другие файлы проекта, кроме tax-config.json и tax-rates-report.md.
- Отчёт tax-rates-report.md нужно записать В ЛЮБОМ случае — даже если ничего не изменилось.
PROMPT

echo "=== Проверка ставок: $(date '+%Y-%m-%d %H:%M') ===" >> "$LOG"

# Разрешения выданы заранее и намеренно узкие: читать интернет, читать и править
# файлы. Ни Bash, ни git — задача физически не может ничего запушить или
# выполнить в системе. Без этого Claude упрётся в запрос разрешения и встанет.
"$CLAUDE" -p "$TASK" \
  --permission-mode acceptEdits \
  --allowedTools "WebSearch,WebFetch,Read,Write,Edit,Glob,Grep" \
  >> "$LOG" 2>&1

echo "" >> "$LOG"
echo "=== Готово: $(date '+%H:%M') ===" >> "$LOG"
echo "" >> "$LOG"
